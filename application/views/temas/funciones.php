<p class="bd-lead" id="funciones">Funciones del sistema esquelético</p>
<div class="row">
	<div class="col">
		<ol>
			<li><b>Sostén: </b>Los huesos son el soporte de los tejidos blandos, y el punto de apoyo de la mayoría de los músculos esqueléticos.</li>
			<li><b>Protección: </b>Los huesos protegen a los órganos internos, por ejemplo el cráneo protege al encéfalo, la caja torácica al corazón y pulmones.</li>
			<li><b>Movimientos: </b>En conjunto con los músculos.</li>
			<li><b>Homeostasis de minerales: </b>El tejido óseo almacena calcio y fósforo para dar resistencia a los huesos, y también los libera a la sangre para mantener en equilibrio su concentración.</li>
			<li><b>Producción de células sanguíneas: </b>En la médula ósea roja (tejido conectivo especializado) se produce la hemopoyesis para producir glóbulos rojos, blancos y plaquetas.</li>
			<li><b>Almacenamiento de triglicéridos: </b>La médula ósea roja es reemplazada paulatinamente en los adultos por médula ósea amarilla, que contiene adipocitos.</li>
		</ol>
	</div>
</div>