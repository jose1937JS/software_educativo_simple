<p class="bd-lead">Estructura de los huesos</p>
<div class="row">
	<div class="col">
		<p>Los huesos se clasifican en diversos tipos según su forma. Un hueso largo (como el fémur o el húmero) consta de las siguientes partes: </p>
		<ol>
			<li><b>Diáfisis: </b>Es el cuerpo o porción cilíndrica principal del hueso.</li>
			<li><b>Epífisis: </b>Son los extremos proximal y distal del hueso.</li>
			<li><b>Metáfisis: </b>Es el sitio de unión de la diáfisis con la epífisis; su espesor va disminuyendo con la edad.</li>
			<li><b>Cartílago articular: </b>Es una capa delgada de cartílago hialino que cubre la parte de la epífisis de un hueso que se articula con otro hueso.</li>
			<li><b>Periostio: </b>Es una capa resistente de tejido conectivo denso que rodea la superficie ósea que no tiene cartílago articular. Protege al hueso, participa en la reparación de fracturas, colabora en la nutrición del hueso, y sirve como punto de inserción de tendones y ligamentos.</li>
			<li><b>Cavidad medular: </b>Es el espacio interno de la diáfisis que contiene a la médula ósea amarilla grasa.</li>
			<li><b>Endostio: </b>Es la capa que recubre la cavidad medular, y contiene células formadoras de hueso.</li>
		</ol>
