<p class="bd-lead" id="compacto">Tejido óseo compacto</p>
<div class="row">
	<div class="col">
		<p>Forma la capa externa de todos los huesos; brinda protección y sostén. Está formado por unidades llamada osteonas o sistemas de Havers, que constan de:</p>
		<ul class="default">
			<li>Un conducto central que tiene un trayecto longitudinal y que contiene un vaso sanguíneo, llamado <b>conducto de Havers</b>.</li>
			<li>Una serie de <b>laminillas</b> concéntricas que rodean al conducto de Havers, que son anillos de matriz dura calcificada.</li>
			<li><b>Lagunas</b>, que son espacios ubicados entre los anillos de las laminillas, y que contienen osteocitos.</li>
			<li><b>Canalículos</b> que se irradian desde las lagunas en todas direcciones, llenos de líquidos extracelular, y que contienen delgadas prolongaciones de los osteocitos; comunican a las lagunas entre sí y con los conductos centrales.</li>
		</ul>
		<p>Las osteonas son circulares y no se ajustan perfectamente entre ellas, y las zonas que quedan entre las osteonas están llenas de <b>laminillas intersticiales</b> y <b>laminillas circunferenciales</b>.</p><p> Los vasos sanguíneos y linfáticos y los nervios provenientes del periostio penetran en el hueso compacto, por los <b>conductos perforantes de Volkmann</b>.</p>
