<p class="bd-lead" id="diferencias">Diferencias entre los esqueletos masculino y femenino</p>
<div class="row">
	<div class="col">
		<p>Hay diferencias generales y específicas. La diferencia general es de tamaño y peso, el del varón es más pesado y grande. </p><p>Las diferencias específicas se refieren a la forma de los huesos pélvicos y a la cavidad pelviana. La pelvis masculina es profunda e infundibuliforme, con arco púbico angosto (menor a 90°). La pelvis femenina es ancha y poco profunda y extendida hacia los lados, con ángulo púbico amplio (mayor a 90°).</p>
