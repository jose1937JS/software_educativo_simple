<p class="bd-lead" id="histologia">Histología del tejido óseo</p>
<div class="row">
	<div class="col">
		<p>Tiene una matriz abundante, y células muy separadas entre sí.</p>
		<p>La matriz está formada por:</p>
		<ul class="default">
			<li>25% de agua.</li>
			<li>25% de fibras proteínicas.</li>
			<li>50% de sales minerales cristalizadas.</li>
		</ul>
		<p>Las células son: </p>
		<ul class="default">
			<li><b>Células osteógenas: </b>Son células madre, no especializadas, con capacidad de división; sus células hijas son los osteoblastos; se localizan en la porción interna del periostio y del endostio.</li>
			<li><b>Osteoblastos: </b>Son las células que construyen los huesos; sintetizan los componentes de la matriz del tejido óseo e inician en proceso de calcificación. (sufijo blasto indica células que secretan matriz)</li>
			<li><b>Osteocitos: </b>Son las células maduras principales del tejido óseo; derivan de los osteoblastos que quedan atrapados en la matriz; intercambian nutrientes con la sangre. (sufijo cito indica células constituyentes de los tejidos)</li>
			<li><b>Osteoclastos: </b>Son células muy grandes, formadas por la fusión de 50 monocitos, ubicadas en el endostio; producen destrucción del hueso por medio de enzimas lisosómicas para permitir el desarrollo, crecimiento, mantenimiento y reparación normales del hueso. (sufijo clasto indica destrucción)</li>
		</ul>
		<p>La dureza del hueso depende de las sales minerales orgánicas cristalizadas que contiene, y su flexibilidad depende de las fibras colágenas. </p><p>Los huesos no son completamente sólidos, ya que tienen muchos espacios. Según el tamaño y distribución de estos espacios, las regiones de un hueso se clasifican en compactas y esponjosas. En general el hueso compacto constituye el 80% del esqueleto, y el esponjoso el 20% restante.</p>
	</div>
</div>