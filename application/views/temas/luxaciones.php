<p class="bd-lead" id="luxaciones">Luxaciónes</p>
<div class="row">
	<div class="col">
		<p>Una luxación o dislocación es toda lesión cápsulo-ligamentosa con pérdida del contacto de las superficies articulares por causa de un trauma grave, que puede ser total (luxación) o parcial (subluxación). En semiología clínica, el término se conoce como abartrosis o abarticulación.</p><p> En medicina, una dislocación es una separación de dos huesos en el lugar donde se encuentran en la articulación.</p>
