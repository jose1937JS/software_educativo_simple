<p class="bd-lead" id="lordosis">Lordosis</p>
<div class="row">
	<div class="col">
		<p>La lordosis es la curvatura fisiológica de la columna en la región cervical o lumbar. La columna presenta cuatro curvaturas fisiológicas (o "normales"): dos curvaturas hacia afuera, en la columna dorsal (al nivel de las costillas) y en la columna sacra, denominadas cifosis y dos curvaturas lordóticas (hacia adentro de la columna): la lordosis lumbar y la cervical. Las curvas escolióticas (curvatura hacia los lados) siempre se consideran patológicas.</p>
