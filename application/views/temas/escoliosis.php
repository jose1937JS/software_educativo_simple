<p class="bd lead" id="escoliosis">Escoliosis</p>
<div class="row">
	<div class="col">
		<p>Es la curvatura de la columna vertebral, que se presenta en forma de "S" o de "C". Generalmente se clasifica en congénita (causada por anomalías vertebrales presentes al nacer), idiopática (de causa desconocida, sub-clasificada a su vez como infantil, juvenil, adolescente o adulto según la fecha de inicio se produjo) o neuromuscular (habiéndose desarrollado como síntoma secundario de otra enfermedad espina bífida, parálisis cerebral, atrofia muscular espinal o un trauma físico). </p><p>La escoliosis afecta a aproximadamente 7 millones de personas en los Estados Unidos.</p>
