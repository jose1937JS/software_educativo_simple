<p class="bd-lead" id="cifosis">Cifosis</p>
<div class="row">
	<div class="col">
		<p>Es la curvatura fisiológica de la columna vertebral en la región dorsal. La columna presenta cuatro curvaturas fisiológicas (o "normales"): dos curvaturas dirigidas hacia afuera del cuerpo denominadas cifosis que están presentes en la columna dorsal y sacra, y dos curvaturas llamadas lordosis dirigidas hacia dentro del cuerpo y ubicadas en la región lumbar y cervical. La escoliosis (curvaturas hacia los lados) siempre se considera patológica ("anormal").</p>
