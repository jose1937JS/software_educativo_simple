			<li>El <b>pie plano</b> es un término de uso común que refiere a una enfermedad caracterizada por el colapso del arco del pie, ocasionando que toda (o casi toda) la superficie de la planta tenga contacto con el suelo.</li>
			<li>El <b>pie cavo</b> es una anomalía con el pie arqueado. Las personas con esta condición colocan demasiado peso y esfuerzo sobre el metatarso del pie y talón del pie cuando están de pie o caminando. El desarrollo de esta condición puede suceder a cualquier edad.</li>
