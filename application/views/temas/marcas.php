<p class="bd-lead" id="marcas">Marcas superficiales de los huesos.</p>
<div class="row">
	<div class="col">
		<p>Son rasgos estructurales adaptados a funciones específicas. Aparecen donde se insertan los tendones, ligamentos y fascias, o donde las arterias perforan el hueso; otras dan paso a un tendón. Ellas son:</p>
		<ul class="default">
			<li>Cóndilo: zona articular redondeada (p.ej., cóndilo femoral lateral)</li>
			<li>Cresta: borde del hueso (p.ej., cresta ilíaca)</li>
			<li>Epicóndilo: eminencia de un cóndilo (p.ej., epicóndilo lateral del húmero)</li>
			<li>Carilla: zona suave y lisa, cubierta por cartílago, donde el hueso de articula con otro (p.ej., fosita costal superior del cuerpo vertebral para la articulación con una costilla)</li>
			<li>Orificio: paso óseo (p.ej., orifico obturador)</li>
			<li>Fosa: zona hueca o deprimida (p.ej., fosa infraespinosa de la escápula)</li>
			<li>Surco: depresión alargada o acanalada (p.ej., surcos arteriales de la calota craneal)</li>
			<li>Línea: elevación lineal (p.ej., línea sólea de la tibia)</li>
			<li>Maléolo: prominencia redondeada (p.ej., maléolo lateral del peroné)</li>
			<li>Escotadura: indentación en el borde de un hueso (p.ej., escotadura ciática mayor)</li>
			<li>Protuberancia: proyección ósea (p.ej., protuberancia occipital externa)</li>
			<li>Espina: prolongación en forma de aguja (p.ej., espina de la escápula)</li>
			<li>Apófisis espinosa: parte que se proyecta en forma de espina (p.ej.,apófisis espinosa de una vértebra)</li>
			<li>Trocánter: gran elevación roma (p.ej., trocánter mayor del fémur)</li>
			<li>Tubérculo: pequeña eminencia elevada (p.ej., tubérculo mayor del húmero)</li>
			<li>Tuberosidad: gran elevación redondeada (p.ej., tuberosidad isquiática)</li>
		</ul>
	</div>
</div>