<p class="bd-lead" id="cartilago">Cartílago</p>
<div class="row">
	<div class="col">
		<p>Es un tipo de tejido conectivo especializado, elástico, carente de vasos sanguíneos, formados principalmente por matriz extracelular y por células dispersas denominadas condrocitos. La parte exterior del cartílago, llamada pericondrio, es la encargada de brindar el soporte vital a los condrocitos.</p>
		<p>Posee más sustancia intercelular que células. No tiene irrigación capilar propia, por eso sus células (los condrocitos) reciben el oxígeno y los nutrientes por difusión desde el pericondrio (revestimiento fibroso).</p>
