<p class="bd-lead" id="clasificacion">Clasificación de los huesos</p>
<div class="row">
	<div class="col">
		<p>Según su forma, los huesos se clasifican en:</p>
		<ol>
			<li>Huesos largos, que son tubulares, constan de diáfisis y epífisis. Tiene hueso compacto en la diáfisis y hueso esponjoso en el interior de las epífisis. Por ejemplo: el húmero del brazo.</li>
			<li>Huesos cortos, que son cuboidales, tiene tejido esponjoso salvo en su superficie. Por ejemplo: huesos del tarso y del carpo.</li>
			<li>Huesos planos, son delgados compuestos por dos placas casi paralelas de tejido óseo compacto que envuelven a otra de hueso esponjoso. Brindan protección. Por ejemplo: huesos del cráneo, esternón, omóplatos.</li>
			<li>Huesos irregulares, que tiene forma compleja. Por ejemplo: vértebras y algunos huesos de la cara.</li>
			<li>Huesos sesamoideos, están en algunos tendones, a los que protegen del uso y desgarro excesivos. Por ejemplo: la rótula.</li>
		</ol>
